$('.value-1').keyup(function() {
    var result = 0;
    $('#age-bracket-col-1-total').attr('value', function() {
        $('.value-1').each(function() {
            if ($(this).val() !== '') {
                result += parseInt($(this).val());
            }
        });
        return result;
    });
})

